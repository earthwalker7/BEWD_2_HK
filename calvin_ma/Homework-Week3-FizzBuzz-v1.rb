1.upto(100) do | number |

if number % 15 == 0
	number = "FizzBuzz"
elsif 
	number % 5 == 0 
	number = "Buzz"
elsif 
	number % 3 == 0 
	number = "Fizz"
	else
	end

puts number 

end


